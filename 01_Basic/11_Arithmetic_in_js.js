//  in addition to those basic arithematic operator JS support more complex mathematical operation through a set of function and constants
console.log(Math.pow(2,5));
console.log(Math.round(2.6));// round to the nearest interger
console.log(Math.ceil(.6)); // round to upto an interger
console.log(Math.floor(.6)); // round down to an interger
console.log(Math.abs(-90)); // give the absolute value
console.log(Math.max(4,56,7));// return the largest argument
console.log(Math.min(-90,2,-19)); // return the minimum argument 
console.log(Math.random()); // give the random number between 0 and 1
console.log(Math.sqrt(20)); // return the squart root of the number
console.log(Math.pow(3,1/3)); // the cube root of 3
console.log(Math.sin(0)); // Trignometry : also Math.cos, Math.atan
console.log(Math.log(10)); // Natural log of 10
console.log(Math.exp(3)); // Math.E cubed
//   Arithmetic in js doesnot raise error in case of overflow , underflow or division by zero
console.log(10/0) // this give infinity
console.log(0/0) // gives NaN (Not-a-Number)


